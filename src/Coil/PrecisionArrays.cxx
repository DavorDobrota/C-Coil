    int maxSize = 500;
    int cnt = 0;

    for (int i = 1; i <= maxN; ++i)
        if (i % (i / 65 + 1) == 0)
        {
            printf("%d, ", i / (i / 65 + 1)); cnt++;
        }


    for (int i = maxN; cnt < maxSize; ++i)
    {
        printf("64, "); cnt++;
    }

